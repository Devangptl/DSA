package Operator;

public class unarry {
    public static void main(String[] args) {
        // Incriment unarry operators
        int a = 10;
        int b = 10;
        int c = 10;
        int d = 10;
        int preIncrement = ++a;
        int postIncrement = b++;
        int preDecriment = --c;
        int postDecriment = d++;
        System.out.println(preIncrement);
        System.out.println(postIncrement);
        System.out.println(preDecriment);
        System.out.println(postDecriment);

    }
}
