public class PrimitivDataType {
    public static void main(String args[]) {

        // Primitiv Data type

        byte b = 8; // Size = 1 byte --- [-128 to 127]
        System.out.println("Byte : " + b);

        short sh = 10; // Size = 2 byte --- [sort value]
        System.out.println("Short : " + sh);

        char ch = 'a'; // Size = 2 byte --- [a-z , A-Z ,special char ex: @ ,% , etc]
        System.out.println("char : " + ch);

        boolean boo = true; // Size = 1 byte --- [true ya false]
        System.out.println("boolean : " + boo);

        int a = 10; // size = 4 byte --- [-2 bilion to 2 bilion]
        System.out.println("int : " + a);

        // long --> largest intiger number store , Size = 8 byte ---[largest intiger
        // number]

        float flo = 0.2f;// Size = 4 byte --- [.]
        System.out.println("float : " + flo);

        // bouble --> largest floting value , Size = 8 byte
    }
}