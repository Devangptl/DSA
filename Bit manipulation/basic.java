public class basic {
    public static void main(String[] args) {

        System.out.println(5 & 4); // Binary AND

        System.out.println(5 | 4); // Binary OR

        System.out.println(5 ^ 4); // Binary XOR

        // not opterator
        System.out.println((~5)); // 1'S Compliment
        System.out.println((~0));

        // Binary Left Shift
        System.out.println(5 << 2); // use the formuls a<<b = a * 2^b

        // Binary Rigth Shift
        System.out.println(5 >> 2); // use formula a>>b = a/2^b
    }
}